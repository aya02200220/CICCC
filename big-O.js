// O(n) Linear time
function addUpTo(n) {
  let total = 0;
  for (let i = 1; i <= n; i++) {
    total += i;
  }
  return total;
}
console.log(addUpTo(5)); // 15

// O(1) Constant time
function addUpTo2(n) {
  return (n * (n + 1)) / 2;
}
console.log(addUpTo2(5)); // 15

